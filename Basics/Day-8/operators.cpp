// Arithmetic Operators

#include <iostream>
using namespace std;

int main()
{
    int a = 10, b = 3;
    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
    cout << "a % b = " << a % b << endl;
    return 0;
}

// Relational (Comparison) Operators
#include <iostream>
using namespace std;

int main()
{
    int x = 5, y = 10;
    cout << (x == y) << endl; // 0 (false)
    cout << (x != y) << endl; // 1 (true)
    cout << (x < y) << endl;
    cout << (x > y) << endl;
    cout << (x <= y) << endl;
    cout << (x >= y) << endl;
}
